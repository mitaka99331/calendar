<?php

namespace App\Repository;

use App\Entity\Appointment;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Appointment>
 *
 * @method Appointment|null find($id, $lockMode = null, $lockVersion = null)
 * @method Appointment|null findOneBy(array $criteria, array $orderBy = null)
 * @method Appointment[]    findAll()
 * @method Appointment[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class AppointmentRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Appointment::class);
    }

    public function save(Appointment $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Appointment $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function findByMonth(\DateTime $dateTime): array
    {
        $fromDate = new \DateTime($dateTime->format('Y-m'));
        $toDate = new \DateTime($dateTime->format('Y-m'));

        return $this->createQueryBuilder('a')
            ->andWhere('a.date >= :fromDate')
            ->setParameter('fromDate',$fromDate )
            ->andWhere('a.date <= :toDate')
            ->setParameter('toDate',$toDate->modify('+1 month'))
            ->select('a.date')
            ->getQuery()
            ->getResult()
        ;
    }

